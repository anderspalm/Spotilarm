public class neewObject {

    String hello, no;

    public neewObject(String hello, String no) {
        this.hello = hello;
        this.no = no;
    }

    public String getHello() {
        return hello;
    }

    public void setHello(String hello) {
        this.hello = hello;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
